void solve() {
    ll n;
    cin >> n;
    vector<ll> v(n);
    for (int i = 0; i < n; i++)
        cin >> v[i];
    if (n == 2) {
        cout << v[1] << " " << v[0] << '\n';
        return;
    }
    for (auto i : v) {
        if (i == n)
            cout << i << " ";
        else
            cout << n - i << " ";
    }
    cout << '\n';
}

